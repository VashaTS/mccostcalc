function craft($item,$qty,$ft){
	global $recipe,$fuel;
	$items=Array();
	$itemsq=Array();
	$nitems=Array();
	$nitemsq=Array();
	$items[0]=$item;
	$itemsq[0]=$qty;
	do{
		$allend=0;
		foreach($items as $k=>$v){
			if(!isset($recipe[$v])) echo('Error processing item: <b>'.$v.'</b>. Please report this to sajmon313@gmail.com<br>');
			else{
				if($recipe[$v]!='end'){
					$allend+=1;
					$tmp=explode(',',$recipe[$v]);
					foreach($tmp as $t){
						$t2=explode(':',$t);
						$nitems[count($nitems)]=$t2[0];
						$nitemsq[count($nitemsq)]=($t2[1]*$itemsq[$k]);
					}
				}
				else if($v=='fuel'){
					$allend+=1;
					$nitems[count($nitems)]=$ft;
					$nitemsq[count($nitemsq)]=($itemsq[$k]/$fuel[$ft]);
				
				}
				else{
					$nitems[count($nitems)]=$v;
					$nitemsq[count($nitemsq)]=$itemsq[$k];
				}
			}
		}
		$items=$nitems;
		$itemsq=$nitemsq;
		$nitems=Array();
		$nitemsq=Array();
	}while($allend!=0);
	foreach($items as $key=>$val){
		$re[$val]+=$itemsq[$key];
	}
	return $re;
